#Decisions

print("Lean the steps of the 5 sequence tango.")
print("What steps do you wish to learn?")

steps_to_learn = input()

if steps_to_learn == "1":
    print("Leader takes a steps back")

elif steps_to_learn == "2":
    print("Side step towards centre of floor")

elif steps_to_learn == "3":
    print("Leader steps outside the follower")

elif steps_to_learn == "4":
    print("Preparation of the cross with the forward step.")

elif steps_to_learn == "5":
    print("Lead closes his feet, follower completes cross step.")

else:
    print("Terminate the sequence")